#include <iostream>
#include <cctype>

int	main(int argc, char **argv)
{
	int	i;
	int	j;

	if (argc == 1)
		std::cout << "* LOUD AND UNBEARABLE FEEDBACK NOISE *" << std::endl;
	else
	{
		i = 1;
		while (i < argc)
		{
			j = 0;
			while (argv[i][j])
				std::cout << static_cast<char>(std::toupper(argv[i][j++]));
			i++;
		}
		std::cout << std::endl;
	}
	return (0);
}
