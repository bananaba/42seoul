#include "libftprintf.h"

char	*ft_itoa(int n)
{
	char		l[100];
	long		a;
	int			i;

	a = n;
	if (n < 0)
		a = -a;
	else if (n == 0)
		return (ft_strdup("0"));
	i = -1;
	l[99] = '\0';
	while (a > 0 && ((i++) || 1))
	{
		l[98 - i] = a % 10 + '0';
		a /= 10;
	}
	if (n < 0)
		l[98 - ++i] = 45;
	return (ft_strdup(&l[98 - i]));
}
